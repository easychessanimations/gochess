func CastlingLetter(color utils.PieceColor, side utils.CastlingSide) string {
	letter := "k"

	if side == utils.QUEEN_SIDE {
		letter = "q"
	}

	if color == utils.WHITE {
		letter = strings.ToUpper(letter)
	}

	return letter
}

