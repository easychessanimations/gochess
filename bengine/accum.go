// member functions

func (a *Accum) add(s Score) {
	a.M += s.M
	a.E += s.E
}

func (a *Accum) addN(s Score, n int32) {
	a.M += s.M * n
	a.E += s.E * n
}

func (a *Accum) merge(o Accum) {
	a.M += o.M
	a.E += o.E
}

func (a *Accum) deduct(o Accum) {
	a.M -= o.M
	a.E -= o.E
}

