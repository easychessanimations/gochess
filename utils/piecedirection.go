func (pd PieceDirection) ToString() string {
	dirStr, ok := PIECE_DIRECTION_TO_DIRECTION_STRING[pd]

	if ok {
		return dirStr
	}

	return ""
}

func (pd *PieceDirection) EqualTo(testpd PieceDirection) bool {
	return (pd.File == testpd.File) && (pd.Rank == testpd.Rank)
}

