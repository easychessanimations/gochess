func (p *PieceColor) ToString() string {
	if *p == WHITE {
		return "w"
	}

	return "b"
}

func (p *PieceColor) SetFromFen(fen string) {
	if fen == "w" {
		*p = WHITE
		return
	}

	*p = BLACK
}

func (p *PieceColor) Inverse() PieceColor {
	return WHITE - *p
}

